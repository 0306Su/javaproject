public class Dome05 {
    public static void main(String[] args) {
        //运算符 operator ： + - * / %
        int a = 10;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(1.0 * a / b);
        System.out.println(a / b * 1.0);
        System.out.println(a * 1.0 / b);
        System.out.println(a % b);


    }
}
