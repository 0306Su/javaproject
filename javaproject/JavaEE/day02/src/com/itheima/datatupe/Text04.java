public class Text04 {
    public static void main(String[] args) {
//        ​	2.1、学员数量，可以用默认的整数类型int表示
//
//​	2.2、商品价格，使用默认的浮点数类型double表示
//
//​	2.3、天文数字，可以使用长整型long表示
//
//​	2.4、一个描述真或者假，只有两种状态，可以使用boolean类型
        int a = 100;
        double b= 55.55;
        long c= 460000000L;
        boolean d = true;
        System.out.println("学员数量，可以用默认的整数类型int表示"+a);
        System.out.println("商品价格，使用默认的浮点数类型double表示"+b);
        System.out.println("天文数字，可以使用长整型long表示"+c);
        System.out.println("一个描述真或者假，只有两种状态，可以使用boolean类型"+d);
    }
}
